package fundamentals;

public class DataTypes {
	 public static void main(String[] args) {
	 byte b = 100;
	 short s = 1000;
	 int i = 50000;
	 long l = 100000L;
	 float f = 10.5f;
	 double d = 99.99;
	 char c = 'A';
	 boolean bool = true;
	 System.out.println(b + " " + s + " " + i + " " + l + " " + f + " " + 
	d + " " + c + " " + bool);
	 }
	}
	