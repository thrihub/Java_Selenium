package fundamentals;

public class Defaults {
	int i;
	 boolean b;
	 char c;
	 public static void main(String[] args) {
	 Defaults obj = new Defaults();
	 System.out.println("int: " + obj.i); // 0
	 System.out.println("boolean: " + obj.b); // false
	 System.out.println("char: '" + obj.c + "'"); 
	 }
	}
	
